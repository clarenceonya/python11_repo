#Python Comparison operators
x=10
y=4
#equality operator. equality operator is the double equal symbol while assignment operator is just one equal symbol(x=1)
print(x==y)

#greater than operator
print(x>y)

#greater than or equal to operator
print(x>=y)

#less than operator
print(y<x)
#less than or equal to
print(x<=y)

       

