#Write a program that asks the user to enter two numbers and compares them using the following comparison operators: `>`, `<`, `>=`, `<=`, `==`, and `!=`. 
# The program should print out the result of each comparison.

#write two variables and assign numbers
paul=50 
mary=30

#the greater than operator
print(paul>mary)

#the less than operator
print(mary<paul)

#the greater than or equal to operator
print(paul>=mary)

#the less than or equal to operator
print(mary<=paul)

#the equality operator
print(paul==mary)

#the not equal to operator
print(paul!=mary)